#[test]
fn test_deterministic_rng() {
    let mut rng1 = MockRng::new(100);
    let mut rng2 = MockRng::new(100);

    let mut buf1 = [0u8; 32];
    let mut buf2 = [0u8; 32];

    rng1.fill_bytes(&mut buf1);
    rng2.fill_bytes(&mut buf2);

    assert_eq!(buf1, buf2, "MockRng should be deterministic");

    // Verify first few bytes are sequential starting from 100
    assert_eq!(buf1[0], 100);
    assert_eq!(buf1[1], 101);
    assert_eq!(buf1[2], 102);
}

