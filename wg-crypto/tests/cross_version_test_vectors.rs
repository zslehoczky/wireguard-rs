#[test]
fn test_public_keys_match() {
    let sk1 = StaticSecret::from(SK1_BYTES);
    let pk1 = PublicKey::from(&sk1);
    assert_eq!(pk1.as_bytes(), &PK1_BYTES, "Public key 1 mismatch");

    let sk2 = StaticSecret::from(SK2_BYTES);
    let pk2 = PublicKey::from(&sk2);
    assert_eq!(pk2.as_bytes(), &PK2_BYTES, "Public key 2 mismatch");
}

#[test]
fn test_initiation_message_matches_old_version() {
    // Setup device 1 (initiator)
    let sk1 = StaticSecret::from(SK1_BYTES);
    let pk2 = PublicKey::from(PK2_BYTES);

    let mut dev1: Device<usize, MockInstant, OldVersionTimestamp> = Device::new();
    dev1.set_sk(Some(sk1));
    dev1.add(pk2, 1).unwrap();
    dev1.set_psk(pk2, SecretBytes(PSK)).unwrap();

    // Create initiation with deterministic RNG
    let mut rng = MockRng::new(100);
    let now = MockInstant::default();
    let msg_init = dev1.begin(now, &mut rng, &pk2).unwrap();

    // Compare with expected vector
    let expected_bytes = hex::decode(EXPECTED_INITIATION).unwrap();
    let expected = Initiation::parse(&expected_bytes[..]).unwrap();
    let actual = Initiation::parse(msg_init.as_ref()).unwrap();

    assert_eq!(*actual, *expected, "Initiation message mismatch\nExpected: {:#?}\nActual: {:#?}", *expected, *actual);
}

#[test]
fn test_response_message_matches_old_version() {
    // Setup device 2 (responder)
    let sk2 = StaticSecret::from(SK2_BYTES);
    let pk1 = PublicKey::from(PK1_BYTES);

    let mut dev2: Device<usize, MockInstant, OldVersionTimestamp> = Device::new();
    dev2.set_sk(Some(sk2));
    dev2.add(pk1, 2).unwrap();
    dev2.set_psk(pk1, SecretBytes(PSK)).unwrap();

    // Process initiation
    let initiation_bytes = hex::decode(EXPECTED_INITIATION).unwrap();
    let mut rng = MockRng::new(200);
    let now = MockInstant::default();

    let result = dev2.process(now, &mut rng, &initiation_bytes, None);

    assert!(result.is_ok(), "Failed to process initiation: {:?}", result.err());

    let Output { id: _, msg, key_pair } = result.unwrap();

    // Verify we got a response message
    assert!(msg.is_some(), "No response message generated");

    if let Some(msg_response) = msg {
        let expected_bytes = hex::decode(EXPECTED_RESPONSE).unwrap();
        let expected = Response::parse(&expected_bytes[..]).unwrap();
        let actual = Response::parse(msg_response.as_ref()).unwrap();

        assert_eq!(*actual, *expected, "Response message mismatch\nExpected: {:#?}\nActual: {:#?}", *expected, *actual);
    }

    // Verify keypair was created
    assert!(key_pair.is_some(), "No keypair generated");

    if let Some(kp) = key_pair {
        assert_eq!(kp.initiator, false, "Responder keypair should have initiator=false");

        let send_key_hex = hex::encode(kp.send.key.as_ref());
        let recv_key_hex = hex::encode(kp.recv.key.as_ref());

        assert_eq!(
            send_key_hex, EXPECTED_INITIATOR_RECV_KEY,
            "Responder send key mismatch"
        );
        assert_eq!(
            recv_key_hex, EXPECTED_INITIATOR_SEND_KEY,
            "Responder recv key mismatch"
        );
    }
}

#[test]
fn test_complete_handshake_matches_old_version() {
    // Setup device 1 (initiator)
    let sk1 = StaticSecret::from(SK1_BYTES);
    let pk1 = PublicKey::from(&sk1);
    let pk2 = PublicKey::from(PK2_BYTES);

    let mut dev1: Device<usize, MockInstant, OldVersionTimestamp> = Device::new();
    dev1.set_sk(Some(sk1));
    dev1.add(pk2, 1).unwrap();
    dev1.set_psk(pk2, SecretBytes(PSK)).unwrap();

    // Setup device 2 (responder)
    let sk2 = StaticSecret::from(SK2_BYTES);

    let mut dev2: Device<usize, MockInstant, OldVersionTimestamp> = Device::new();
    dev2.set_sk(Some(sk2));
    dev2.add(pk1, 2).unwrap();
    dev2.set_psk(pk1, SecretBytes(PSK)).unwrap();

    let now = MockInstant::default();

    // Step 1: Create initiation
    let mut rng1 = MockRng::new(100);
    let msg_init = dev1.begin(now, &mut rng1, &pk2).unwrap();

    let expected_init_bytes = hex::decode(EXPECTED_INITIATION).unwrap();
    let expected_init = Initiation::parse(&expected_init_bytes[..]).unwrap();
    let actual_init = Initiation::parse(msg_init.as_ref()).unwrap();

    assert_eq!(*actual_init, *expected_init, "Initiation mismatch\nExpected: {:#?}\nActual: {:#?}", *expected_init, *actual_init);

    // Step 2: Process initiation and create response
    let mut rng2 = MockRng::new(200);
    let Output { id: _id2, msg: msg_opt, key_pair: kp2 } =
        dev2.process(now, &mut rng2, msg_init.as_ref(), None).unwrap();

    assert!(msg_opt.is_some(), "No response message");
    let msg_response = msg_opt.unwrap();

    let expected_response_bytes = hex::decode(EXPECTED_RESPONSE).unwrap();
    let expected_response = Response::parse(&expected_response_bytes[..]).unwrap();
    let actual_response = Response::parse(msg_response.as_ref()).unwrap();

    assert_eq!(*actual_response, *expected_response, "Response mismatch\nExpected: {:#?}\nActual: {:#?}", *expected_response, *actual_response);

    assert!(kp2.is_some(), "Responder keypair not created");
    let kp2 = kp2.unwrap();
    assert_eq!(kp2.initiator, false);

    // Step 3: Process response
    let Output { id: _id1, msg: msg_opt, key_pair: kp1 } =
        dev1.process(now, &mut rng1, msg_response.as_ref(), None).unwrap();

    assert!(msg_opt.is_none(), "Unexpected message after processing response");
    assert!(kp1.is_some(), "Initiator keypair not created");
    let kp1 = kp1.unwrap();
    assert_eq!(kp1.initiator, true);

    // Verify keys match
    assert_eq!(kp1.send.key.as_ref(), kp2.recv.key.as_ref(), "Send/recv key mismatch (initiator->responder)");
    assert_eq!(kp1.recv.key.as_ref(), kp2.send.key.as_ref(), "Send/recv key mismatch (responder->initiator)");

    // Verify IDs match
    assert_eq!(kp1.send.id, kp2.recv.id, "Send/recv ID mismatch (initiator->responder)");
    assert_eq!(kp1.recv.id, kp2.send.id, "Send/recv ID mismatch (responder->initiator)");

    // Verify against expected values
    assert_eq!(
        hex::encode(kp1.send.key.as_ref()),
        EXPECTED_INITIATOR_SEND_KEY,
        "Initiator send key mismatch"
    );
    assert_eq!(
        hex::encode(kp1.recv.key.as_ref()),
        EXPECTED_INITIATOR_RECV_KEY,
        "Initiator recv key mismatch"
    );
}

