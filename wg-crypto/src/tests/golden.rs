/// Test that derived keypairs match golden test vectors
#[test]
fn derived_keypairs() {
    #[rustfmt::skip]
    const EXPECTED_SEND_KEY_I: [u8; 32] = [
        0x7b, 0xf8, 0x6f, 0x91, 0xcc, 0x69, 0x23, 0xde,
        0xb4, 0xb0, 0xb7, 0x67, 0xdc, 0x35, 0x5c, 0x81,
        0xe8, 0xd9, 0xf4, 0xc0, 0x40, 0x43, 0xef, 0x73,
        0x05, 0xe6, 0x5c, 0xe4, 0x62, 0xe9, 0xa9, 0xef,
    ];
    #[rustfmt::skip]
    const EXPECTED_RECV_KEY_I: [u8; 32] = [
        0x97, 0x0c, 0xd4, 0x2c, 0xd8, 0x35, 0x88, 0x24,
        0xa8, 0x1d, 0x9d, 0xea, 0x77, 0x70, 0x6e, 0xb1,
        0xf3, 0x3d, 0x6b, 0x63, 0x93, 0x7b, 0xc3, 0x38,
        0x15, 0x41, 0xdd, 0xa4, 0x27, 0xa0, 0xfa, 0x65,
    ];
    #[rustfmt::skip]
    const EXPECTED_SEND_KEY_R: [u8; 32] = [
        0x97, 0x0c, 0xd4, 0x2c, 0xd8, 0x35, 0x88, 0x24,
        0xa8, 0x1d, 0x9d, 0xea, 0x77, 0x70, 0x6e, 0xb1,
        0xf3, 0x3d, 0x6b, 0x63, 0x93, 0x7b, 0xc3, 0x38,
        0x15, 0x41, 0xdd, 0xa4, 0x27, 0xa0, 0xfa, 0x65,
    ];
    #[rustfmt::skip]
    const EXPECTED_RECV_KEY_R: [u8; 32] = [
        0x7b, 0xf8, 0x6f, 0x91, 0xcc, 0x69, 0x23, 0xde,
        0xb4, 0xb0, 0xb7, 0x67, 0xdc, 0x35, 0x5c, 0x81,
        0xe8, 0xd9, 0xf4, 0xc0, 0x40, 0x43, 0xef, 0x73,
        0x05, 0xe6, 0x5c, 0xe4, 0x62, 0xe9, 0xa9, 0xef,
    ];

    let (dev1, pk2) = setup_test_device_1();
    let (dev2, _pk1) = setup_test_device_2();

    let mut rng1 = MockRng::new(0);
    let mut rng2 = MockRng::new(100);
    let now = MockInstant::default();

    let msg_init = dev1.begin(now, &mut rng1, &pk2).unwrap();

    let Output {
        msg: msg_resp,
        key_pair: kp_r,
        ..
    } = dev2
        .process(now, &mut rng2, msg_init.as_ref(), None)
        .expect("failed to process initiation");

    let kp_r = kp_r.expect("no keypair from responder");
    let msg_resp = msg_resp.expect("no response");

    let Output { key_pair: kp_i, .. } = dev1
        .process(now, &mut rng1, msg_resp.as_ref(), None)
        .expect("failed to process response");

    let kp_i = kp_i.expect("no keypair from initiator");

    assert_eq!(
        kp_i.send.key.as_ref(),
        &EXPECTED_SEND_KEY_I,
        "Initiator send key does not match golden test vector"
    );
    assert_eq!(
        kp_i.recv.key.as_ref(),
        &EXPECTED_RECV_KEY_I,
        "Initiator recv key does not match golden test vector"
    );
    assert_eq!(
        kp_r.send.key.as_ref(),
        &EXPECTED_SEND_KEY_R,
        "Responder send key does not match golden test vector"
    );
    assert_eq!(
        kp_r.recv.key.as_ref(),
        &EXPECTED_RECV_KEY_R,
        "Responder recv key does not match golden test vector"
    );

    assert_eq!(kp_i.send, kp_r.recv);
    assert_eq!(kp_i.recv, kp_r.send);
}

